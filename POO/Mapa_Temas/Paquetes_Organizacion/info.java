// Paquetes y organización del código (modificadores de acceso)

// Definición
// Paquete (package): Es una forma de organizar el código en carpetas lógicas dentro de un proyecto.
// Ejemplo: package POO.Ejemplo;

// Permite:
// Evitar conflictos entre clases con el mismo nombre.
// Mejorar la organización.
// Controlar la visibilidad entre clases.

// Modificadores de visibilidad
// public → Accesible desde cualquier lugar.
// protected → Accesible dentro del mismo paquete y por clases hijas (subclases).
// sin modificador (package-private) → Accesible solo dentro del mismo paquete.
// private → Accesible solo dentro de la misma clase.