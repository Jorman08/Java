// Definición
// static: Indica que un miembro (variable o método) pertenece a la clase y no a la instancia.
// Atributos estáticos: Compartidos por todas las instancias.
// Métodos estáticos: No necesitan crear un objeto para llamarlos.

// final: Indica que un valor no se puede modificar (constante) o que una clase/método no puede sobrescribirse.
// final en variables → Constantes.
// final en métodos → No se pueden sobrescribir.
// final en clases → No se pueden heredar.

// Se usan para:
// Constantes globales (final static).
// Contadores estáticos (llevar cuenta de instancias creadas).
// Métodos de fábrica estáticos (crear objetos sin new directamente).
