// El encapsulamiento consiste en ocultar los detalles internos de una clase y exponer únicamente lo necesario para interactuar con ella, normalmente mediante métodos públicos (getters y setters)

// Modificadores de acceso
// Determinan la visibilidad de clases, atributos y métodos.

// public → Accesible desde cualquier clase.

// private → Accesible solo dentro de la misma clase.

// protected → Accesible dentro del mismo paquete y por subclases.

// default (sin modificador) → Accesible solo dentro del paquete.

// Getters y Setters
// Son métodos públicos que permiten leer (get) y modificar (set) atributos privados.
// Esto controla cómo se accede y se modifica la información.

// Invariantes de clase
// Son reglas que siempre deben cumplirse para mantener el estado consistente de un objeto.
// Por ejemplo: la edad no puede ser negativa.