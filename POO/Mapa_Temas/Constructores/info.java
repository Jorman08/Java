// Un constructor es un método especial que se utiliza para inicializar objetos cuando se crean.
// Características:

// Tiene el mismo nombre que la clase.

// No retorna ningún valor, ni siquiera void.

// Se ejecuta automáticamente al crear un objeto con new.

// Tipos de constructores:

// Por defecto (default): Lo crea Java automáticamente si no definimos uno.

// Definido por el programador: Cuando lo escribimos nosotros.

// Sobrecargado: Cuando tenemos varios constructores con diferente lista de parámetros.



// 2. Palabra clave this

// this se refiere a la instancia actual del objeto.
// Usos comunes:

// Diferenciar atributos de parámetros cuando tienen el mismo nombre.

// Pasar el objeto actual a otro método.

// Llamar a otro constructor dentro de la misma clase (this()).



// 3. Sobrecarga de métodos

// La sobrecarga (overloading) ocurre cuando en la misma clase hay varios métodos con el mismo nombre, pero con diferentes parámetros (tipo, número o ambos).

